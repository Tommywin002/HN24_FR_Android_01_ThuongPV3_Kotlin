class MyKotlinFunctions {
    fun sumTwoLongNumber(a: Long, b: Long): Long{
        return a + b
    }
    fun subTwoLongNumber(a: Long, b: Long): Long{
        return a - b
    }
    fun multiTwoLongNumber(a: Long, b: Long): Long{
        return a * b
    }
    fun divideTwoLongNumber(a: Long, b: Long): Float{
        return a.toFloat() / b.toFloat()
    }
}

